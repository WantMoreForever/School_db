<?php

/**
 * 数据库连接配置
 *
 * 负责范围：
 * - PDO 连接主机、端口、库名、账号、密码、字符集
 * - 通过环境变量覆盖本地默认值
 *
 * 修改影响：
 * - 会影响整个项目是否能连接数据库
 * - 管理员、教师、学生三端的所有数据读写都会受影响
 *
 * 注意：
 * - 生产环境强烈建议通过环境变量覆盖密码，不要长期依赖文件内默认值。
 */

return [
    // 数据库主机地址。
    // 示例：localhost、127.0.0.1、192.168.1.20
    // 修改影响：应用连接到哪台 MySQL 服务器。
    'host' => getenv('DB_HOST') ?: 'localhost',

    // 数据库端口。
    // 常见值：3306
    // 修改影响：MySQL 连接能否建立。
    'port' => (int) (getenv('DB_PORT') ?: 3306),

    // 数据库名称。
    // 当前项目默认库名：school_db
    // 修改影响：查询的表、视图、存储过程来源。
    'database' => getenv('DB_NAME') ?: 'school_db',

    // 数据库用户名。
    // 示例：root、app_user
    // 修改影响：数据库连接权限、可执行的 SQL 范围。
    'username' => getenv('DB_USER') ?: 'root',

    // 数据库密码。
    // 用途：配合用户名完成连接认证。
    // 修改影响：密码错误会导致全站数据库连接失败。
    // 注意：提交仓库时默认留空，优先通过 DB_PASS 环境变量或本地配置注入真实密码。
    'password' => getenv('DB_PASS') !== false ? (string) getenv('DB_PASS') : '',

    // 连接字符集。
    // 常见值：utf8mb4
    // 修改影响：中文、表情、特殊字符的存储和读取是否正常。
    'charset' => getenv('DB_CHARSET') ?: 'utf8mb4',
];
